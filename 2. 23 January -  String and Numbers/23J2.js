var x = 'Halo Dunia';

console.log(x.length);  //jumlah karakter variable X

console.log(x.indexOf('Dunia')); //D itu ada di index nomor 5 - untuk menyortir variable

console.log(x.substr(5, 3)); //mengambil string dimulai dari index ke-5 sebanyak 3 karakter

console.log(x.slice(5, 8)); //memotong string yg dimulai dari index ke-5 sampai index sebelum index ke-8

console.log(x.split(' ')); //merubah variable menjadi array

