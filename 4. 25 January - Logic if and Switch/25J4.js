//SWITCH function

var job = 'tidur';

switch (job) {
    case 'guru':
    console.log('Kerjaannya ngajar lah.');
    break;
case 'sopir':
    console.log('Kerjaannya Nyetir');
    break;
case 'polisi':
    console.log('Kerjaannya Nilang');
    break;
default:
    console.log('Ada aja Kerjaannya!');
}
